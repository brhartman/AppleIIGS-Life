; CheckKey
; Return: A = #$0001 on key press
; ------------------------------------------------------------------------------
CheckKey     sep       #$30
             ldal      $00c000
             bmi       :KeyPress
:NoKeyPress  stal      $00c010
             rep       #$30
             lda       #$0000
             rts
:KeyPress    stal      $00c010
             rep       #$30
             lda       #$0001
             rts


; WaitKey
; ------------------------------------------------------------------------------
WaitKey      sep       #$30
:Wait        ldal      $00c000
             bpl       :Wait
             stal      $00c010
             rep       #$30
             rts


; CheckError
; ------------------------------------------------------------------------------
CheckError   bcs       ErrorMsg
             rts
ErrorMsg     pha
             PushLong  #0
             _SysFailMgr


; Quit
; ------------------------------------------------------------------------------
Quit
             _QUIT     QuitParam
QuitParam    adrl      $00000000
             da        $0000
